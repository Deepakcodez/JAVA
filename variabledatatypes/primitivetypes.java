public class primitivetypes {
    public static void main(String[] args) {

        // int 
        int num1 = 1;
        int num2 = 2;
        System.err.println(num1+num2);


        // float 
        float fnum1 = 1.5f;
        float fnum2 = 1.5f;
        System.err.println(fnum1 + fnum2);


        //char
        char character = 'D';
        System.err.println(character);

        //string
        String name = "java";
        System.err.println(name);


        //long
        long lnum1 = 15555L;
        long lnum2 = 515555L;
        System.err.println(lnum1 + lnum2);


        //double
         double dnum1 = 155.55d;
        double dnum2 = 51584.5555d;
        System.err.println(dnum1 + dnum2);


        //boolean
        boolean javaisfun = true;
        System.err.println(javaisfun);
        
    }
}
